// Updated helper function to return all locations of a specific color
export const findLocationsByColor = color => {
  return locations.filter(location => location.color === color);
};

// Helper to get a random location from a specific color
export const getRandomLocationByColor = color => {
  const colorLocations = findLocationsByColor(color);
  return colorLocations[Math.floor(Math.random() * colorLocations.length)];
};
